package Com.Class6;

public class ForLoopTask1 {

	public static void main(String[] args) {
		//Print numbers from 1 to 100
		
		for(int a = 1; a<=100; a++) {
			
			System.out.println(a);
		}
       // Print numbers from 100 to 1
		
		for (int b = 100; b >=1; b--) {
			
			System.out.println(b);
		}
		
	}

}
