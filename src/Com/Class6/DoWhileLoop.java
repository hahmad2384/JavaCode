package Com.Class6;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		
		int a = 10;
		
		do { // execute first and then check conditions
			System.out.println("Hello");
			a++;
			
		} while(a > 15); 
			
		int b = 10;
		while (b < 15) { //check condition first and then execute
			System.out.println("Bye");
			b++;
		}
		
	}

}
