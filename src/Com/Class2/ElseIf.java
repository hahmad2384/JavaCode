package Com.Class2;

public class ElseIf {

	public static void main(String[] args) {
		
		int num1 = 30;
		int num2 = 20;
		
if (num1 > num2 ) {
	System.out.println("Num1 is larger than Num2");
	
}else if (num1 < num2) {
	System.out.println("Num1 is smaller than Num2");
	
}else {
	System.out.println("Num1 is equal to Num2");
}
	}

}
